// Ejemplos
//typeof nos devuelve el tipo de la variable sobre la cual lo usemos

let cocodrilo = "Soy un cocodrilo";
console.log(typeof cocodrilo); //string

cocodrilo = 7;
console.log(typeof cocodrilo); //number

cocodrilo = true;
console.log(typeof cocodrilo); //boolean

cocodrilo = undefined;
console.log(typeof cocodrilo); //undefined

cocodrilo = {};
console.log(typeof cocodrilo); //object

